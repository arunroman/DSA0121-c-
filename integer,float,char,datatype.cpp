#include <iostream>
using namespace std;

int main() {
    
    int integerVariable = 10;
    cout << "Integer Variable: " << integerVariable << endl;

    
    float floatVariable = 3.14;
    cout << "Float Variable: " << floatVariable << endl;

    
    char charVariable = 'A';
    cout << "Character Variable: " << charVariable << endl;

    return 0;
}
